// Helper function to get random escort image
export const getRandomEscortImage = (): string => {
  const images = siteImages.gallery.grid;
  return images[Math.floor(Math.random() * images.length)];
};

// Helper function to get image by category
export const getCategoryImage = (category: string): string => {
  switch (category.toLowerCase()) {
    case 'russian':
      return siteImages.russian.hero;
    case 'vip':
      return siteImages.vip.hero;
    case 'model':
      return siteImages.model.hero;
    case 'housewife':
      return siteImages.housewife.hero;
    case 'independent':
      return siteImages.independent.hero;
    default:
      return siteImages.fallback;
  }
};
